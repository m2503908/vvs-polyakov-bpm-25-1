num1 = float(input())
num2 = float(input())
oper = input()

if oper == '+':
    print(f"{num1} {oper} {num2} = {num1 + num2}")
elif oper  == '-':
    print(f"{num1} {oper} {num2} = {num1 - num2}")
elif oper  == '*':
    print(f"{num1} {oper} {num2} = {num1 * num2}")
elif oper  == '/':
    print(f"{num1} {oper} {num2} = {num1 / num2}")
